
import platform

os=platform.system()
print(os)#获取操作系统类型
print(platform.platform())#获取操作系统名称及版本号
print(platform.version())#获取操作系统版本号
print(platform.architecture())#获取操作系统位数
print(platform.machine())#计算机类型
print(platform.node())#计算机网络名称
print(platform.processor())#计算机处理器信息
print(platform.uname())#汇总信息

